-- 食品マスターテーブル（100gあたりの栄養値）
create table if not exists food_master (
  id          serial primary key,
  name_ja     text not null,          -- 食品名（日本語）
  name_kana   text,                   -- 読み仮名（検索用）
  aliases     text[],                 -- 別名・表記ゆれ
  calories    real not null,          -- エネルギー (kcal/100g)
  protein     real not null,          -- タンパク質 (g/100g)
  fat         real not null,          -- 脂質 (g/100g)
  carbs       real not null,          -- 炭水化物 (g/100g)
  category    text not null,          -- カテゴリ
  source      text default 'mext_2020', -- データソース
  created_at  timestamptz default now()
);

create index if not exists idx_food_master_name on food_master using gin(to_tsvector('simple', name_ja));
create index if not exists idx_food_master_category on food_master(category);

-- プレミアムサブスクリプションテーブル
create table if not exists subscriptions (
  id                  uuid primary key default gen_random_uuid(),
  user_id             uuid references auth.users not null unique,
  stripe_customer_id  text,
  stripe_subscription_id text,
  status              text not null default 'inactive',  -- active / inactive / canceled / past_due
  plan                text not null default 'free',      -- free / premium
  current_period_end  timestamptz,
  created_at          timestamptz default now(),
  updated_at          timestamptz default now()
);

alter table subscriptions enable row level security;
create policy "Users can read own subscription" on subscriptions
  for select using (auth.uid() = user_id);

-- user_settings に is_premium カラム追加（高速チェック用）
alter table user_settings
  add column if not exists is_premium boolean default false;

